static int bx_html_entities_replaces(BXString *str) {
  int i = 0;
  int j = 0;
  if (str->value_len == 0 || str->value == NULL) {
    return 0;
  }
  while (i < str->value_len && str->value[i] != '\0') {
    if (str->value[i] != '&') {
      str->value[j++] = str->value[i++];
    } else {
      bool found = false;
      for (int k = 0; BXHTMLEntities[k].entity != NULL; k++) {
        /* if (BXHTMLEntities[k].elen > str.value_len - i ||
         BXHTMLEntities[k].ulen > str.value_len - i) { break;
         }*/

        if (strncmp(BXHTMLEntities[k].entity, &str->value[i],
                    BXHTMLEntities[k].elen) == 0) {
          memset(&str->value[j], 0, BXHTMLEntities[k].elen);
          memcpy(&str->value[j], BXHTMLEntities[k].utf8,
                 BXHTMLEntities[k].ulen);

          i += BXHTMLEntities[k].elen;
          j += BXHTMLEntities[k].ulen;
          found = true;
          break;
        }
      }
      if (!found) {
        str->value[j++] = str->value[i++];
      }
    }
  }
  memset(&str->value[j], 0, str->value_len - j);
  str->value_len = j;
  return 0;
}

